## 💰 実行予算の作成

### 実行予算とは

受注後、実際に現場で使える予算を詳細に積算したもの。

**目的**:
1. 利益目標の設定
2. コスト管理の基準
3. 発注・調達の指針

### 実行予算の構造例

```
【小規模改修工事の実行予算】

売上高: 5,000万円

■ 直接費
  材料費: 1,500万円（30%）
    - 主要資材: 1,200万円
    - 副資材: 300万円

  労務費: 1,200万円（24%）
    - 自社作業員: 800万円（20人×40万円）
    - 応援作業員: 400万円

  外注費: 1,000万円（20%）
    - 電気工事: 400万円
    - 配管工事: 300万円
    - 内装工事: 300万円

  機械費: 300万円（6%）
    - クレーンリース: 150万円
    - 足場リース: 100万円
    - その他機械: 50万円

  直接費小計: 4,000万円（80%）

■ 間接費
  現場管理費: 300万円（6%）
    - 現場事務所: 100万円
    - 光熱費・通信費: 50万円
    - 消耗品: 50万円
    - 安全対策費: 100万円

  一般管理費: 200万円（4%）
    - 本社配賦: 150万円
    - 営業経費: 50万円

  間接費小計: 500万円（10%）

■ 原価合計: 4,500万円（90%）

■ 粗利益: 500万円（粗利率10%）
```

### 実行予算作成のポイント

#### 1. 過去実績の活用

```
【歩掛かりデータ】
内装仕上げ工事: 10㎡/人・日
配管工事: 20m/人・日

※ 自社の過去実績を蓄積し、精度を高める
```

#### 2. 余裕率の確保

```
材料費: 見積もり + 5%（価格変動リスク）
工期: 計画工期 + 10%（天候・トラブルリスク）
```

#### 3. 原価低減目標

```
目標粗利率: 10%
原価低減目標: 2%（90% → 88%）
→ 利益を12%に向上
```

