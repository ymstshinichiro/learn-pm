## 💡 コスト削減の実践

### 悪いコスト削減（品質・安全を犠牲）

❌ **やってはいけない**:
- 安価だが品質の劣る資材
- 経験の浅い作業員の大量投入
- 安全対策の簡素化
- 検査の省略

### 良いコスト削減（工夫による効率化）

✅ **推奨される方法**:

| 手法 | 内容 | 効果 |
|-----|------|------|
| **工法改善** | プレハブ化、ユニット化 | 工期短縮、品質安定 |
| **一括発注** | 複数現場で資材をまとめ発注 | 値引き交渉 |
| **標準化** | 部材の統一、作業手順の統一 | ロス削減、効率化 |
| **ムダ取り** | 待ち時間、手戻り、運搬の削減 | 労務費削減 |
| **デジタル化** | 図面・報告のデジタル化 | 管理工数削減 |

### VE（Value Engineering）

**VEとは**: 機能を維持しながらコストを削減する手法

```
【VEの例】

Before:
  天井仕上げ: 高級石膏ボード + 塗装
  コスト: 5,000円/㎡

After:
  天井仕上げ: 標準石膏ボード + 化粧フィルム
  コスト: 3,500円/㎡（30%削減）

※ 機能（防火性、意匠性）は同等
```

