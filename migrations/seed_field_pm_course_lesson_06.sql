## 📈 出来高管理

### 出来高とは

完成した作業の金額換算値。進捗とコストを同時に把握する指標。

### 出来高の算出方法

#### 1. 物理的出来高

```
配管工事: 100m、単価5万円/m
完了: 60m

出来高 = 60m × 5万円 = 300万円
```

#### 2. 工程別出来高

```
基礎工事: 予算500万円 → 完了 → 出来高500万円
躯体工事: 予算1,000万円 → 50%完了 → 出来高500万円
```

### EVM（Earned Value Management）

**3つの指標**:

| 指標 | 意味 | 記号 |
|-----|------|------|
| **計画価値** | この時点で完了予定の作業の予算 | PV |
| **出来高** | 実際に完了した作業の予算 | EV |
| **実コスト** | 実際にかかったコスト | AC |

**分析指標**:

```
スケジュール差異（SV） = EV - PV
  → プラスなら進捗良好、マイナスなら遅延

コスト差異（CV） = EV - AC
  → プラスならコスト削減、マイナスなら予算超過

コスト効率指標（CPI） = EV / AC
  → 1.0以上なら効率的、1.0未満なら非効率

スケジュール効率指標（SPI） = EV / PV
  → 1.0以上なら順調、1.0未満なら遅延
```

**例**:
```
2ヶ月経過時点
PV（計画出来高）: 2,000万円
EV（実際出来高）: 1,800万円
AC（実コスト）: 1,900万円

SV = 1,800 - 2,000 = -200万円（遅延）
CV = 1,800 - 1,900 = -100万円（予算超過）
CPI = 1,800 / 1,900 = 0.95（5%非効率）
SPI = 1,800 / 2,000 = 0.90（10%遅延）

→ 進捗遅れ、かつコスト超過の状態
```

