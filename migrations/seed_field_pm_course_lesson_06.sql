## 🔧 原価管理の実務

### 日々の原価記録

**記録すべきデータ**:
- 材料使用量（納品書、出庫記録）
- 労務投入量（日報、タイムカード）
- 外注支払い（請求書、検収記録）
- 機械稼働時間（稼働記録）

### 原価管理のデジタル化

| ツール | 用途 |
|-------|------|
| **Excel** | 簡易管理、中小規模 |
| **原価管理ソフト** | 本格管理、大規模 |
| **現場管理アプリ** | 日報入力、写真管理 |
| **会計システム連携** | 自動集計、リアルタイム把握 |

