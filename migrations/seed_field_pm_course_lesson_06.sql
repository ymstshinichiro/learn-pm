## 🎯 原価管理の基本

### 原価の構造

```
売上高
  ↓
▼ 直接費（材料費、労務費、外注費、機械費）
  ↓
▼ 間接費（現場管理費、一般管理費）
  ↓
= 粗利益
```

### コストの分類

| 分類 | 内容 | 例 |
|-----|------|---|
| **直接費** | プロジェクトに直接紐づく | 材料費、労務費、外注費、機械損料 |
| **間接費** | 複数プロジェクトで共有 | 現場事務所費、監督者人件費、保険料 |
| **固定費** | 作業量に関係なく発生 | 現場事務所リース、車両リース |
| **変動費** | 作業量に比例 | 材料費、作業員日当、燃料費 |

