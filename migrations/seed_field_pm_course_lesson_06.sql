## ⚠️ よくある失敗パターン

### 失敗パターン1: 実行予算が甘い

**症状**: 常に予算超過、利益が出ない

**原因**: 見積もりが楽観的、過去実績を無視

**対策**: 過去実績ベース、余裕率確保

### 失敗パターン2: 予実管理が月末のみ

**症状**: 問題発覚が遅れ、手遅れ

**原因**: 日々の記録・集計をしていない

**対策**: 週次での予実チェック

### 失敗パターン3: 追加工事の有償化漏れ

**症状**: 顧客要求に応じたが無償対応、利益減

**原因**: 変更管理が曖昧、交渉力不足

**対策**: 変更管理プロセスの確立、証跡記録

