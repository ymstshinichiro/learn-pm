## 🔍 原因分析

### なぜなぜ分析（5 Whys）

```
【事例: コンクリートにひび割れ】

現象: コンクリートにひび割れ発生

なぜ1: なぜひび割れが発生したのか？
→ 乾燥が早すぎた

なぜ2: なぜ乾燥が早すぎたのか？
→ 養生が不十分だった

なぜ3: なぜ養生が不十分だったのか？
→ 養生シートが不足していた

なぜ4: なぜ養生シートが不足したのか？
→ 発注数量が少なかった

なぜ5: なぜ発注数量が少なかったのか？
→ 施工面積の計算ミスがあった

【根本原因】
材料発注の計算チェック体制がない

【対策】
是正措置: ひび割れ補修
予防措置: 発注時のダブルチェック体制構築
水平展開: 全現場に展開
```

### 4M分析

| 観点 | 内容 | 例 |
|-----|------|---|
| **Man（人）** | 作業員のスキル、疲労、教育 | 未熟な作業員、手順書未確認 |
| **Machine（設備）** | 機械の不具合、メンテナンス | 機械の故障、校正不足 |
| **Material（材料）** | 材料の品質、規格 | 不良材料、規格違い |
| **Method（方法）** | 作業手順、管理方法 | 手順書不備、確認不足 |

