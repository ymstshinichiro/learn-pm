## 🚨 初動対応

### 発見から初動までの流れ

```
トラブル発見
   ↓ 即座に
現場確認（事実把握）
   ↓ 5分以内
上司への第一報
   ↓ 10分以内
応急措置（被害拡大防止）
   ↓ 30分以内
関係者への報告
   ↓ 1時間以内
対策会議
```

### 初動対応のポイント

#### 1. 安全確保が最優先

```
人命 > 設備 > 工期 > コスト

※ まず人の安全を確保、その後に対応
```

#### 2. 現場保存

```
写真撮影: 状況を記録
立入禁止: 証拠保全
関係者確保: 目撃者の証言
```

#### 3. 速やかな報告

```
【第一報の内容】
- いつ（発生日時）
- どこで（場所）
- 何が（トラブル内容）
- 被害状況（人・物）
- 初動措置（実施した対応）
- 今後の対応（予定）

※ 詳細不明でも第一報を優先
```

