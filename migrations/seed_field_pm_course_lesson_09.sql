## ⚠️ よくある失敗パターン

### 失敗パターン1: 隠蔽・報告遅延

**症状**: 小さなトラブルが大問題化

**原因**: 叱責を恐れ、自分で解決しようとする

**対策**: 「早い報告ほど良い」文化、心理的安全性

### 失敗パターン2: 対症療法のみ

**症状**: 同じトラブルが繰り返される

**原因**: 目の前の対処だけ、根本原因を放置

**対策**: なぜなぜ分析、再発防止策の実施

### 失敗パターン3: 責任転嫁

**症状**: 顧客の信頼を失う、訴訟リスク

**原因**: 「協力会社のせい」「天候のせい」

**対策**: 元請としての責任を果たす、誠実な対応

