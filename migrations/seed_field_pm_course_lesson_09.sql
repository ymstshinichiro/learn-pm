## 🎯 トラブル対応の基本原則

### トラブル対応の鉄則

```
1. 迅速な初動対応
2. 事実の正確な把握
3. 関係者への速やかな報告
4. 応急措置と恒久対策の実施
5. 再発防止策の策定と水平展開
```

**最悪のパターン**: 隠蔽、放置、責任転嫁

