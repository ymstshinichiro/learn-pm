## 💼 顧客・近隣対応

### 誠実な対応の原則

| 原則 | 内容 |
|-----|------|
| **迅速性** | 速やかに訪問、謝罪 |
| **誠実性** | 事実を隠さず、正直に説明 |
| **責任感** | 言い訳せず、改善を約束 |
| **継続性** | 一度きりでなく、フォローアップ |

### 近隣への配慮

**工事開始前**:
```
- 近隣挨拶（工事内容・期間・連絡先）
- 騒音・振動の説明
- 作業時間の説明（8時-18時等）
```

**工事中**:
```
- 定期的な挨拶
- クレーム発生時の速やかな対応
- 清掃・美化（道路の清掃、泥落とし）
```

**工事完了後**:
```
- 完了の挨拶
- 協力への感謝
```

