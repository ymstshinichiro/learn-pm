## ⚠️ 労働災害発生時の対応

### 災害発生時の初動対応

```
【緊急時の行動】

1. 救護（1分以内）
   - 負傷者の救護
   - 救急車の手配（119番）

2. 二次災害防止（即座）
   - 作業停止
   - 危険箇所の立入禁止

3. 報告（5分以内）
   - 所長への報告
   - 元請への報告
   - 労働基準監督署への報告（死亡・休業4日以上）

4. 現場保存
   - 写真撮影
   - 証拠保全

5. 原因調査
   - 災害調査委員会の設置
```

### 労災報告書の作成

```
【労働者死傷病報告】

1. 発生状況
   - 日時、場所、作業内容

2. 災害の型
   - 墜落、挟まれ、感電など

3. 原因
   - 直接原因、間接原因

4. 対策
   - 是正措置、再発防止策

※ 労働基準監督署に提出（遅滞なく）
```

