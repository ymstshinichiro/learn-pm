## 😠 クレーム対応

### クレームの種類

| 分類 | 内容 | 例 |
|-----|------|---|
| **品質クレーム** | 仕上がり不良、性能不足 | 傷、色ムラ、動作不良 |
| **工期クレーム** | 納期遅延 | 約束の日に完成せず |
| **騒音・振動** | 近隣迷惑 | 早朝・深夜の作業音 |
| **安全** | 危険な状況 | 通行人への危険 |
| **対応** | 態度、説明不足 | 無愛想、連絡なし |

### クレーム対応の5ステップ

#### ステップ1: 傾聴・共感

```
❌ 悪い対応:
「それは仕様です」
「気のせいではないですか」
「他の現場では問題ないです」

✅ 良い対応:
「ご不便をおかけして申し訳ございません」
「詳しくお聞かせください」
「お気持ちはよく分かります」
```

**まず相手の話を最後まで聞く、遮らない**

#### ステップ2: 事実確認

```
- 具体的に何が問題か
- いつから発生しているか
- どのような状況か
- 写真・現物で確認
```

#### ステップ3: 原因究明と対応策提示

```
「原因を調査いたします」
「〇〇日までに対応策をご提示します」

※ その場で約束できない場合は期限を明確に
```

#### ステップ4: 是正措置の実施

```
- 速やかに補修・交換
- 進捗を報告
- 完了を確認してもらう
```

#### ステップ5: 再発防止とフォローアップ

```
「今後このようなことがないよう、
 〇〇の対策を実施いたします」

※ 後日、「その後いかがですか？」とフォロー
```

### クレーム対応のNG行動

| NG行動 | 理由 |
|-------|------|
| **否定・言い訳** | 相手の怒りを増幅 |
| **責任転嫁** | 「協力会社のせい」→信頼失墜 |
| **放置** | 小さなクレームが大きくなる |
| **過剰約束** | 実現できず、二次クレーム |

