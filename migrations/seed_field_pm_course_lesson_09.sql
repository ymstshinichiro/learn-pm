## 📋 トラブル記録と水平展開

### トラブル事例データベース

```
【記録項目】
- 発生日時・場所
- トラブル内容
- 原因
- 対応措置
- 被害（コスト・工期）
- 再発防止策
- 添付資料（写真・図面）
```

**活用**:
- 類似トラブルの予防
- 新人教育の教材
- 保険請求の資料

### 水平展開

```
トラブル発生
   ↓
原因分析・対策
   ↓
自現場で対策実施
   ↓
他の現場にも共有（水平展開）
   ↓
全社で同じトラブルを防ぐ
```

