## 🗣️ コミュニケーションスキル

### 1on1ミーティング

**目的**: 個別の対話で、悩み・不満を把握、モチベーション向上

**頻度**: 月1回、30分程度

**内容**:
```
1. 近況確認: 体調、家族、プライベート
2. 仕事の状況: 困っていること、悩み
3. 目標の確認: 今後どうなりたいか
4. フィードバック: 良い点、改善点
5. サポート: 何か支援できることは？
```

**ポイント**:
- リーダーが8割聞く、2割話す
- 評価の場ではなく、支援の場
- プライベートな場所で、リラックスして

### フィードバックの技術

#### ポジティブフィードバック

```
「今日の配管作業、寸法がピッタリでした。
 丁寧に測定した結果ですね。ありがとう！」

※ 具体的に、タイムリーに
```

#### 改善フィードバック（SBI法）

```
Situation（状況）: 今日の検査の場面で
Behavior（行動）: チェックリストを使わずに
Impact（影響）: 1項目見落としがありました

「今後はチェックリストを必ず使ってください。
 見落とし防止になります」

※ 人格否定ではなく、行動に焦点
```

