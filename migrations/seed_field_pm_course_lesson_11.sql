## 👥 チームビルディング

### チーム発展の5段階

```
1. 形成期（Forming）
   メンバーが集まる、様子見

2. 混乱期（Storming）
   意見の対立、役割の不明確さ

3. 統一期（Norming）
   ルールが定まる、協力体制

4. 機能期（Performing）
   高いパフォーマンス発揮

5. 散会期（Adjourning）
   プロジェクト終了、解散
```

**リーダーの役割**: 各段階に応じた支援

### 心理的安全性の確保

**心理的安全性とは**: 失敗やミスを報告しても、非難されない環境

#### 心理的安全性の高いチーム

✅ **特徴**:
- 問題を隠さず報告
- 積極的な質問・提案
- 互いに助け合う
- 失敗から学ぶ

#### 心理的安全性の低いチーム

❌ **特徴**:
- 問題を隠す、報告しない
- 質問・提案を躊躇
- 責任の押し付け合い
- 失敗を恐れ、挑戦しない

### 心理的安全性を高める方法

| 手法 | 内容 |
|-----|------|
| **傾聴** | メンバーの意見を最後まで聞く |
| **感謝** | 報告・提案に感謝を伝える |
| **失敗の許容** | 「失敗は学びの機会」と伝える |
| **率先垂範** | リーダー自ら失敗を認め、学ぶ |

