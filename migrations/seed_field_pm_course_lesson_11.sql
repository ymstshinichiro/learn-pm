## 🌱 次世代リーダーの育成

### 育成の3段階

```
1. やって見せる
   （リーダーが手本を示す）

2. やらせてみる
   （本人にやってもらう、見守る）

3. 振り返る
   （フィードバック、改善点の指摘）
```

### OJT（On-the-Job Training）

**計画的なOJT**:

```
【新人の育成計画例】

1ヶ月目: 基本作業の習得
- 工具の使い方、安全ルール
- 先輩とペアで作業

2-3ヶ月目: 単独作業の開始
- 簡単な作業を単独で
- 完了後に先輩が確認

4-6ヶ月目: 応用作業への挑戦
- 複雑な作業にも挑戦
- 定期的なフィードバック

6ヶ月後: 独り立ち評価
```

### 権限委譲（エンパワーメント）

**段階的な権限委譲**:

```
レベル1: 指示通りに実行
レベル2: 選択肢から選んで実行
レベル3: 自分で考えて提案、承認後実行
レベル4: 自分で判断して実行、事後報告
レベル5: 完全に任せる

※ 能力に応じて段階的に
```

### メンタリング

**メンター制度**:
- 経験豊富な先輩（メンター）が若手（メンティ）を支援
- 業務以外の相談も可
- キャリア形成のアドバイス

