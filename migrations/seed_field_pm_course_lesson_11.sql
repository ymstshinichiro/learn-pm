## 🎯 現場リーダーの役割

### リーダーに求められる3つの役割

| 役割 | 内容 |
|-----|------|
| **管理者** | 工程・品質・コスト・安全の管理 |
| **リーダー** | チームをまとめ、方向性を示す |
| **育成者** | メンバーを育て、次世代を作る |

### マネジメントとリーダーシップの違い

| 観点 | マネジメント | リーダーシップ |
|-----|------------|--------------|
| **焦点** | 仕組み、プロセス | 人、ビジョン |
| **手法** | 計画、管理、統制 | 鼓舞、共感、方向性 |
| **問い** | どうやって達成するか | なぜやるのか、どこへ向かうのか |
