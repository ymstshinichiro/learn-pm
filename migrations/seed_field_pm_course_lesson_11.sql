## 💪 モチベーション管理

### マズローの欲求5段階

```
5. 自己実現欲求
   （自分の可能性を発揮したい）
      ↑
4. 承認欲求
   （認められたい、尊重されたい）
      ↑
3. 社会的欲求
   （仲間に属したい、愛されたい）
      ↑
2. 安全欲求
   （安全・安定した環境）
      ↑
1. 生理的欲求
   （食事、睡眠、休息）
```

**現場への適用**:
- 1-2段階: 安全確保、適切な休憩
- 3段階: チームの一体感
- 4段階: 表彰、感謝の言葉
- 5段階: 挑戦の機会、成長

### ハーズバーグの二要因理論

| 要因 | 内容 | 例 |
|-----|------|---|
| **衛生要因** | 不満を防ぐ（あって当たり前） | 給与、労働環境、安全 |
| **動機づけ要因** | 満足を生む（向上心） | 達成感、承認、成長 |

**教訓**: 給与だけでは満足しない。達成感・承認・成長機会が必要

### モチベーション向上の実践

#### 1. 目標の共有

```
悪い例: 「とにかく頑張れ」

良い例: 「今月中にA棟完成、無災害達成。
        そうすれば顧客も喜ぶし、ボーナスも出る。
        みんなで頑張ろう！」
```

#### 2. 承認と感謝

```
- 朝礼で良い仕事を褒める
- 「ありがとう」を日常的に
- 月間MVPの表彰
```

#### 3. 成長の機会

```
- 新しい工法の習得
- 資格取得の支援
- 責任ある仕事を任せる
```

