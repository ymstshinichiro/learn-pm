## ⚠️ よくある失敗パターン

### 失敗パターン1: マイクロマネジメント

**症状**: すべてを細かく指示、部下が育たない

**原因**: 信頼不足、完璧主義

**対策**: 権限委譲、失敗を許容

### 失敗パターン2: 放任主義

**症状**: 「任せた」と言って放置、トラブル多発

**原因**: 育成を怠る、責任回避

**対策**: 適度な支援、定期確認

### 失敗パターン3: えこひいき

**症状**: 特定のメンバーだけ優遇、チーム分裂

**原因**: 好き嫌い、私情

**対策**: 公平な評価、透明性

