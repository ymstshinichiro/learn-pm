## 📢 ヒヤリハット管理

### ハインリッヒの法則（再掲）

```
重大災害 1件
    ↑
軽微な事故 29件
    ↑
ヒヤリハット 300件
```

**教訓**: ヒヤリハットを見逃さず、対策することで重大災害を防ぐ

### ヒヤリハット報告の仕組み

#### 報告しやすい環境づくり

❌ **悪い文化**:
- ヒヤリハットを報告すると叱責される
- 「注意力が足りない」と個人の責任にする
- 報告しても何も変わらない

✅ **良い文化**:
- ヒヤリハット報告を推奨、表彰する
- 個人を責めず、システムの問題として扱う
- 報告に基づき迅速に対策、フィードバック

#### ヒヤリハット報告書

```
【報告項目】
1. 日時・場所
2. 作業内容
3. 何が起きたか（ヒヤリハットの状況）
4. 原因（なぜ起きたか）
5. 対策（今後どうするか）
```

#### ヒヤリハットの分析と対策

```
【事例】
足場の端材につまずき、転倒しそうになった（ヒヤリハット）

【原因分析】
- 作業後の清掃が不十分
- 端材を放置する習慣

【対策】
- 作業終了時の5S（整理・整頓・清掃・清潔・躾）を徹底
- 端材置き場を明確化
- 朝礼で注意喚起
```

