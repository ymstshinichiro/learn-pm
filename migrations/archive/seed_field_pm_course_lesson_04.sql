## 🗣️ KY活動（危険予知活動）

### KY活動とは

作業開始前に、チーム全員で「どんな危険が潜んでいるか」を話し合い、対策を確認する活動。

### KY活動の4ラウンド

```
【4R-KYT（危険予知訓練）】

1R: 現状把握
   「どんな危険が潜んでいるか」
   → チーム全員でブレインストーミング

2R: 本質追究
   「これが危険のポイントだ」
   → 最も重要な危険を絞り込む

3R: 対策樹立
   「あなたならどうする」
   → 具体的な対策をリストアップ

4R: 目標設定
   「私たちはこうする」
   → チーム行動目標を決定
```

### KY活動の実施例

**作業**: 屋根上での瓦交換作業

```
1R: 現状把握
- 屋根から墜落する
- 瓦を踏んで滑る、破損する
- 工具を落とす
- 熱中症になる

2R: 本質追究
「屋根からの墜落」が最も重大

3R: 対策樹立
- 安全帯を正しく使用する
- 足場を確実に設置する
- 2人1組で作業する
- 悪天候時は中止する

4R: 目標設定
「安全帯のフック掛け替え時も必ず2か所掛け！」
```

