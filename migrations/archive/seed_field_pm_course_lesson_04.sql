## 🎯 安全管理の基本原則

### 安全第一の意味

```
安全 > 品質 > 工期 > コスト

※ 安全だけは絶対に譲れない
```

**理由**:
1. **人命は何物にも代えられない**
2. **災害は全てを止める**: 工事中断、企業の信用失墜
3. **法的責任**: 労働安全衛生法違反、刑事責任
4. **経済的損失**: 補償金、訴訟費用、受注機会喪失

### 労働災害の種類

| 災害分類 | 主な原因 | 例 |
|---------|---------|---|
| **墜落・転落** | 高所作業、足場不備 | 屋根からの転落、開口部への転落 |
| **挟まれ・巻き込まれ** | 機械作業、重機 | クレーン作業、プレス機 |
| **飛来・落下** | 上部作業からの落下物 | 工具・資材の落下 |
| **崩壊・倒壊** | 掘削、足場 | 土砂崩れ、足場崩壊 |
| **感電** | 電気工事 | 活線作業、漏電 |
| **切れ・こすれ** | 刃物、機械 | カッター、研磨機 |

