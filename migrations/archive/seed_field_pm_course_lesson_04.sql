## 📋 安全計画の立案

### 安全計画書の構成

| 項目 | 内容 |
|-----|------|
| **1. 安全目標** | 無災害達成、目標無災害日数 |
| **2. 安全管理体制** | 責任者、安全担当者、緊急連絡先 |
| **3. リスクアセスメント** | 危険源、評価、対策 |
| **4. 安全教育計画** | 新規入場者教育、定期教育 |
| **5. 安全活動** | 朝礼、KY活動、安全パトロール |
| **6. 緊急時対応** | 事故発生時の手順、連絡先 |
| **7. 安全設備** | 保護具、消火器、救急箱 |

### 安全管理体制

```
【現場の安全管理体制例】

現場所長（統括安全衛生責任者）
    ↓
安全管理者
    ↓
各作業チームリーダー
    ↓
作業員
```

**役割**:
- **現場所長**: 安全の最終責任者
- **安全管理者**: 日々の安全管理、パトロール、教育
- **チームリーダー**: チーム内の安全確認、KY活動
- **作業員**: 安全ルール遵守、ヒヤリハット報告

