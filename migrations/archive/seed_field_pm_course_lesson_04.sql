## 🚨 安全パトロール

### 安全パトロールの目的

- 不安全な状態・行動の早期発見
- 安全意識の向上
- ルール遵守の確認

### パトロールのチェックポイント

| 分類 | チェック項目 |
|-----|------------|
| **保護具** | ヘルメット、安全帯、安全靴の着用 |
| **足場** | 手すり、ネット、踏み板の状態 |
| **機械** | 安全カバー、スイッチ、点検記録 |
| **整理整頓** | 通路、資材置き場、廃材処理 |
| **掲示** | 安全標識、立入禁止表示 |

### 是正指示と フォローアップ

```
発見 → 即座に是正指示 → 是正確認 → 記録

※ 危険度の高いものは即座に作業停止
```

