## ⚠️ よくある失敗パターン

### 失敗パターン1: 形式的な安全活動

**症状**: KYやパトロールが形骸化、効果なし

**原因**: 「やればいい」という姿勢、チェックリストの消化

**対策**: 現場の実態に即した活動、トップの本気度

### 失敗パターン2: ベテラン作業員の慢心

**症状**: 経験豊富な作業員が事故に遭う

**原因**: 「自分は大丈夫」という過信、ルール軽視

**対策**: ベテランこそ安全教育、相互注意の文化

### 失敗パターン3: 工期優先で安全を犠牲

**症状**: 「急いでいるから」と安全対策を省略

**原因**: 工期遅延のプレッシャー、安全<工期の誤った優先順位

**対策**: 「安全は絶対条件」の徹底、工期より安全

