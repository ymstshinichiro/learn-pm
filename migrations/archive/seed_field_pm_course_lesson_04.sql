## 🔍 リスクアセスメントの実践

### リスクアセスメントとは

作業開始前に「危険源」を特定し、リスクを評価し、対策を講じる一連のプロセス。

### リスクアセスメントの手順

#### ステップ1: 危険源の特定

作業ごとに「何が危険か」を洗い出します。

**洗い出し手法**:
- **現場確認**: 実際の作業場所を見る
- **作業分解**: 作業を細かいステップに分解
- **過去事例**: 類似作業での事故事例を参照
- **チェックリスト**: 業界標準のリスト活用

**例: 屋根改修工事**
```
危険源:
- 屋根からの墜落
- 工具の落下
- 屋根材の破損・踏み抜き
- 悪天候（強風、雨、雷）
- 熱中症（夏季の屋根上作業）
```

#### ステップ2: リスクの評価

危険源ごとに「発生確率」と「影響度」を評価します。

**リスクマトリクス**:

```
影響度
 大 │  中   高   高
    │
 中 │  低   中   高
    │
 小 │  低   低   中
    └─────────────
       低   中   高
         発生確率
```

**評価基準例**:

| 発生確率 | 説明 | レベル |
|---------|------|--------|
| **高** | 対策しなければほぼ確実に発生 | 3 |
| **中** | 時々発生する可能性 | 2 |
| **低** | めったに発生しない | 1 |

| 影響度 | 説明 | レベル |
|-------|------|--------|
| **大** | 死亡・重傷、長期休業 | 3 |
| **中** | 中程度の負傷、数日休業 | 2 |
| **小** | 軽傷、応急処置で対応 | 1 |

**リスクレベル = 発生確率 × 影響度**

```
例: 屋根からの墜落
発生確率: 中（2）
影響度: 大（3）
リスクレベル: 2 × 3 = 6（高リスク）
```

#### ステップ3: リスク対策の検討

リスクレベルに応じて対策を立案します。

**対策の優先順位（安全対策の階層）**:

```
1. 危険源の除去（本質的安全）
   → 高所作業をなくす（地上で組み立て）

2. 工学的対策（設備で防ぐ）
   → 安全柵、手すり、ネット設置

3. 管理的対策（ルールで防ぐ）
   → 作業手順書、有資格者限定、立入禁止

4. 個人用保護具（最後の砦）
   → ヘルメット、安全帯、保護メガネ
```

**例: 屋根墜落対策**
```
1. 本質的安全: 地上で作業できる部分は地上で実施
2. 工学的対策: 手すり付き足場設置、墜落防止ネット
3. 管理的対策: 安全帯使用義務、悪天候時中止、2人1組作業
4. 保護具: ヘルメット、安全帯（フルハーネス）
```

#### ステップ4: 残留リスクの評価

対策後もゼロにならない「残留リスク」を評価し、許容可能か判断します。

```
【対策前】
墜落リスク: 高（6）

【対策後】
手すり・ネット・安全帯で対策
→ 発生確率: 低（1）、影響度: 中（2）
→ 残留リスク: 2（低）

判断: 許容可能、ただし安全帯の正しい使用を徹底
```

