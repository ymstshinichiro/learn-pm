## 🏆 改善提案制度

### 提案制度の目的

1. **現場の知恵を活かす**: ボトムアップの改善
2. **モチベーション向上**: 自分の提案が採用される喜び
3. **改善文化の醸成**: 全員参加の風土

### 提案制度の運営

#### 提案様式

```
【改善提案書】

1. 提案者: 氏名、所属
2. 提案日:
3. テーマ: 〇〇の改善
4. 現状の問題点:
5. 改善案:
6. 期待効果: （コスト削減、工期短縮、品質向上等）
7. 実施の容易性: （すぐできる、検討必要等）
```

#### 評価と表彰

```
【評価基準】
- 効果の大きさ（コスト削減額等）
- 独創性
- 実現可能性
- 他への展開性

【表彰】
- 優秀賞: 年間最優秀提案（賞金・賞状）
- 奨励賞: 良い提案（図書カード等）
- 参加賞: 提案者全員（記念品）
```

