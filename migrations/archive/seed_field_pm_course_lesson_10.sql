## 🔄 PDCAサイクル

### PDCAの4ステップ

```
Plan（計画）
  ↓
Do（実行）
  ↓
Check（評価）
  ↓
Act（改善）
  ↓
（繰り返し）
```

### 実践例: 検査時間の短縮

```
【Plan】
現状: 検査に1時間かかる
目標: 30分に短縮
方法: チェックリストの見直し、重点項目に絞る

【Do】
新しいチェックリストで1週間試行

【Check】
結果: 平均40分に短縮
課題: 一部項目で見落としあり

【Act】
改善: 重点項目は維持、見落とし項目を追加
次回: さらなる効率化を検討
```

