## 📊 現場データの活用

### データの種類

| データ | 内容 | 用途 |
|-------|------|------|
| **進捗データ** | 出来高、工程達成率 | 工期管理 |
| **原価データ** | 材料費、労務費 | コスト管理 |
| **品質データ** | 不良率、検査結果 | 品質管理 |
| **安全データ** | ヒヤリハット、無災害日数 | 安全管理 |

### データの可視化

#### 1. グラフ化

```
【棒グラフ】
月別の不良件数の推移
→ 増減傾向が一目瞭然

【折れ線グラフ】
日別の進捗率
→ 計画線との乖離を確認

【円グラフ】
コスト構成比
→ どこにコストがかかっているか
```

#### 2. 管理図

```
【例: 検査時間の管理図】

時間
80分 ┤
     │      ●       上限管理線
60分 ┤  ●   ●  ●   中心線
     │●   ●      ●
40分 ┤              下限管理線
     └──────────────→ 日数

※ 管理線を超えたら異常、要調査
```

#### 3. パレート図

```
【不良原因のパレート図】

件数
30 ┤■■■
   │■■■
20 │■■■ ■■
   │■■■ ■■ ■
10 │■■■ ■■ ■ ■
   └─────────────
    A    B   C  その他
   (50%) (25%)(15%)(10%)

→ A対策で50%削減可能（80:20の法則）
```

### データに基づく意思決定

**定性的判断**:
「なんとなく遅れている気がする」

**定量的判断**:
「進捗率85%、計画より5%遅延、2日の挽回必要」

**データの力**: 客観的な現状把握と的確な対策

