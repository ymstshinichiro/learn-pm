## 👥 QCサークル活動

### QCサークルとは

**定義**: 小グループで品質管理・改善活動を自主的に行う

**特徴**:
- 5-10名程度の小集団
- 月1-2回の定例会
- テーマを設定し、数ヶ月で改善

### QCサークルの進め方

#### ステップ1: テーマ選定

```
【テーマ例】
- 検査時間の短縮
- 不良率の削減
- 資材ロスの削減
- 安全性の向上
```

**選定基準**:
- 現場の課題に直結
- メンバーで改善可能
- 効果が測定可能

#### ステップ2: 現状把握

```
データ収集:
- 検査時間: 平均60分
- ばらつき: 45-80分
- ボトルネック: 測定に時間

グラフ化:
- ヒストグラム
- パレート図
```

#### ステップ3: 目標設定

```
目標: 検査時間を30分に短縮
期限: 3ヶ月後
```

#### ステップ4: 要因分析

```
特性要因図（魚の骨図）:

          測定に時間
         /    |    \
       /      |      \
    人      機械    方法
（未熟）（古い）（非効率）
```

#### ステップ5: 対策立案と実施

```
対策案:
1. 測定機器の更新
2. 作業手順の見直し
3. 教育訓練の実施

優先順位:
2 → 3 → 1（コストの小さいものから）
```

#### ステップ6: 効果確認

```
結果: 平均40分に短縮（33%削減）
コスト: ゼロ（手順見直しと教育のみ）
```

#### ステップ7: 標準化と水平展開

```
成功した手順を標準作業手順書に反映
他の現場にも展開
```

