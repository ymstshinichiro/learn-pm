## ⚠️ よくある失敗パターン

### 失敗パターン1: 改善が続かない

**症状**: 最初は盛り上がるが、すぐに元に戻る

**原因**: 標準化していない、チェックしていない

**対策**: 改善を標準作業手順書に反映、定期確認

### 失敗パターン2: トップダウンの押し付け

**症状**: 現場がやらされ感、形骸化

**原因**: 経営層が一方的に指示

**対策**: 現場主導、自主的な活動を支援

### 失敗パターン3: データ収集が目的化

**症状**: データは集めるが、活用しない

**原因**: 何のためにデータを取るか不明確

**対策**: 目的を明確に、改善に活かす

