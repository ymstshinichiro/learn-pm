## 🗑️ ムダ取りの7つのムダ

### トヨタ生産方式の7つのムダ

| ムダ | 内容 | 現場の例 |
|-----|------|---------|
| **作りすぎ** | 必要以上に生産 | 過剰な資材発注 |
| **手待ち** | 作業員が待機 | 前工程の遅延で待機 |
| **運搬** | 不要な移動 | 資材置き場が遠い |
| **加工** | 過剰な品質 | 顧客要求を超える仕上げ |
| **在庫** | 必要以上の在庫 | 使わない資材の保管 |
| **動作** | 無駄な動き | 工具の取りに行く動作 |
| **不良** | 手戻り、スクラップ | 検査不合格、再施工 |

### ムダ取りの実践

#### 例: 手待ちムダの削減

```
【現状】
配管作業員が電気工事完了を待って1時間待機

【改善】
工程表を詳細化し、電気工事の完了予定を30分単位で共有
→ 配管作業員は他の準備作業を実施
→ 待ち時間ゼロ
```

#### 例: 運搬ムダの削減

```
【現状】
資材置き場が現場から50m離れており、往復に時間

【改善】
仮設資材置き場を作業エリア近くに設置
→ 往復時間が1/5に削減
```

