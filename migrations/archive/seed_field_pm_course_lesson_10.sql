### ベンチマーキングとは

**定義**: 優れた他社・他現場の手法を学び、自現場に取り入れる

### 実践方法

#### 1. 内部ベンチマーキング

```
自社の優良現場を訪問
- 何が違うのか
- どうやって実現しているか
- 自現場に適用できるか
```

#### 2. 外部ベンチマーキング

```
業界の先進事例を学ぶ
- 見学会、セミナー参加
- 業界誌、論文の調査
- 自社に合うようカスタマイズ
```

