## 📱 情報共有ツールの活用

### アナログツール

| ツール | 用途 | メリット |
|-------|------|---------|
| **掲示板** | 工程表、安全目標の掲示 | 一目で分かる、全員が見る |
| **ホワイトボード** | 本日の作業、連絡事項 | 書き換え容易 |
| **黒板** | 工事写真用の情報表示 | 記録に残る |

### デジタルツール

| ツール | 用途 | メリット |
|-------|------|---------|
| **LINE/チャット** | 即時連絡、写真共有 | リアルタイム、記録残る |
| **現場管理アプリ** | 日報、写真、進捗管理 | 一元管理、自動集計 |
| **クラウドストレージ** | 図面、書類の共有 | 最新版の一元管理 |

### ツール活用の注意点

**デジタルの落とし穴**:
- 高齢の作業員がついていけない
- 現場で確認しづらい（Wi-Fi不安定）
- 重要な情報が流れて埋もれる

**バランスが重要**:
- 重要事項は掲示板（アナログ）
- 日常連絡はチャット（デジタル）
- 記録はアプリ（デジタル）

