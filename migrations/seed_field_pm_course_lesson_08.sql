## 🌅 朝礼の運営

### 朝礼の目的

1. **本日の作業共有**: 誰が・何を・どこで
2. **安全喚起**: KY活動、注意事項
3. **情報共有**: 変更点、来客予定
4. **チームの一体感**: 全員で顔を合わせる

### 効果的な朝礼の進め方

```
【朝礼の流れ（15分）】

1. 出席確認（1分）
   - 全業者の作業員が揃っているか

2. 所長挨拶（2分）
   - 本日の重点事項、激励

3. 安全唱和（1分）
   - 「安全第一！」全員で唱和

4. KY活動（5分）
   - 本日の作業の危険予知、対策確認

5. 作業内容確認（5分）
   - 各業者のリーダーが本日の作業を報告
   - 重複・干渉する作業の調整

6. 連絡事項（1分）
   - 来客、検査予定、天候予報

7. ラジオ体操（オプション）
```

### 朝礼の形骸化を防ぐ

❌ **悪い朝礼**:
- 所長が一方的に話すだけ
- 毎日同じ内容の繰り返し
- 参加者が聞いていない

✅ **良い朝礼**:
- 各業者が発言、双方向
- その日固有の情報（天気、作業内容）
- 短く、テンポよく

