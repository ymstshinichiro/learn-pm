## ⚠️ よくある失敗パターン

### 失敗パターン1: 悪いニュースを隠す

**症状**: 問題が表面化した時には手遅れ

**原因**: 叱責を恐れ、報告を躊躇

**対策**: 心理的安全性、「早い報告ほど良い」文化

### 失敗パターン2: 伝言ゲーム

**症状**: 情報が伝わる過程で歪む、漏れる

**原因**: 口頭のみの伝達、確認不足

**対策**: 文書化、直接伝達、復唱確認

### 失敗パターン3: 会議が長い・結論出ない

**症状**: 会議が2時間、何も決まらない

**原因**: 議題不明確、発言が脱線

**対策**: 議題の事前共有、ファシリテーター、時間管理

