## 📢 報連相（ホウレンソウ）の基本

### 報連相とは

| 用語 | 意味 | タイミング |
|-----|------|----------|
| **報告** | 指示された仕事の結果を伝える | 完了時、中間報告 |
| **連絡** | 関係者に情報を伝える | 情報入手時、即座に |
| **相談** | 判断に迷った時に意見を求める | 判断前、早めに |

### 報連相のルール

#### 1. 報告のルール

**5W2H で具体的に**:
```
When（いつ）: 今日14時に
What（何が）: 基礎工事が
Where（どこで）: A棟で
Who（誰が）: 〇〇建設が
Why（なぜ）: コンクリート硬化待ちのため
How（どのように）: 予定通り
How much（いくら）: 予算内で
```

**悪い報告**: 「順調です」「問題ありません」（曖昧）

**良い報告**: 「A棟基礎工事、本日14時完了。予算内、明日から躯体工事着手可能」

#### 2. 連絡のルール

**迅速性が命**:
- 良いニュースも悪いニュースも即座に
- 「後で」は禁物、その場で連絡
- 関係者全員に漏れなく

#### 3. 相談のルール

**早めに、選択肢を持って**:
```
悪い相談: 「どうしましょうか？」（丸投げ）

良い相談:
「配管ルートで問題発生。
 案1: ルート変更（費用+10万、工期+2日）
 案2: 既存配管移設（費用+20万、工期+3日）
 私は案1を推奨しますが、ご判断いただけますか？」
```

