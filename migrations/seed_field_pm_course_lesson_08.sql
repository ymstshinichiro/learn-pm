## 🎓 まとめ

コミュニケーションは「空気を読む」ではなく、「仕組み化」するものです。

### 成功のための5つのポイント

1. **報連相の徹底**: 5W2Hで具体的に、迅速に
2. **朝礼・定例会議**: 短く、テンポよく、双方向
3. **情報の見える化**: 掲示板、ツールで共有
4. **記録の習慣**: 日報、議事録で証跡を残す
5. **心理的安全性**: 悪いニュースも報告しやすい文化

「現場は生き物、情報は血液」— 情報が滞れば、現場は機能不全に陥ります。
