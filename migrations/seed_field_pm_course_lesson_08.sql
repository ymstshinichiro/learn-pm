## 🗣️ コミュニケーションスキル

### 伝え方の工夫

#### 1. 結論ファースト

```
悪い例:
「昨日から配管作業をしていまして、途中で既存配管を発見し、調査したところ、図面に記載がなく、ルート変更が必要で...」

良い例:
「配管ルート変更が必要です。理由は既存配管の発見です。対応案は2つあり...」
```

#### 2. 視覚化

- 図・写真を使う
- 数値で示す
- 実物を見せる

#### 3. 確認

```
伝えた後: 「理解いただけましたか？」
聞いた後: 「確認ですが、〇〇ということですね？」
```

### 聴き方の工夫

#### 1. 傾聴

- 途中で遮らない
- うなずき、相づち
- メモを取る

#### 2. 質問

```
オープン質問: 「どう思いますか？」（自由回答）
クローズド質問: 「AとBどちらですか？」（選択）

※ 状況に応じて使い分け
```

