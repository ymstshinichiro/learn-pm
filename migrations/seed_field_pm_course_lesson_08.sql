## 📅 定例会議の運営

### 会議の種類

| 会議 | 頻度 | 参加者 | 目的 |
|-----|------|--------|------|
| **デイリーミーティング** | 毎日 | 所長、各リーダー | 当日の進捗、翌日の予定 |
| **週次工程会議** | 週1回 | 全業者、発注者 | 進捗報告、来週の予定、調整 |
| **月次定例会議** | 月1回 | 全業者、発注者、設計者 | 月次総括、来月計画、課題 |

### 効果的な会議の進め方

#### 1. 事前準備

```
会議の3日前:
- 議題を整理し、資料を準備
- 参加者に議題を事前共有

会議の前日:
- 参加者に再度リマインド
- 資料を配布
```

#### 2. 会議の進行

```
1. 開会（1分）
   - 議題確認、時間配分

2. 報告事項（20分）
   - 進捗、品質、安全、コスト

3. 協議事項（30分）
   - 問題の共有、対策の検討
   - 決定事項の確認

4. 連絡事項（5分）
   - 次回予定、その他

5. 閉会（1分）
   - 決定事項の再確認
```

#### 3. 議事録の作成

```
【議事録のフォーマット】

1. 日時・場所・参加者
2. 議題
3. 報告事項（進捗、課題）
4. 協議内容（発言要旨）
5. 決定事項（誰が・何を・いつまでに）
6. 次回予定

※ 会議後24時間以内に参加者に共有
```

