## 📋 報告書・日報の作成

### 日報の目的

1. **記録**: 何をしたか、トレーサビリティ
2. **進捗共有**: 関係者への情報提供
3. **原価管理**: 人工・材料の集計

### 効果的な日報の書き方

```
【日報フォーマット】

日付: 2024年1月15日（月）天候: 晴れ
作業員: 5名

■ 本日の作業内容
- A棟基礎工事: 配筋完了、検査合格
- B棟: 型枠組立（進捗80%）

■ 明日の予定
- A棟: コンクリート打設
- B棟: 型枠完成、配筋開始

■ 課題・連絡事項
- B棟の鉄筋一部納品遅延、水曜到着予定
- 明日の天気予報は雨、打設は木曜に延期検討

■ 安全事項
- ヒヤリハット: 足場の端材につまずき → 即座に撤去
- KY結果: 打設時の転落注意

■ 材料使用
- コンクリート: 10㎥（累計50㎥/計画100㎥）
- 鉄筋: 2t（累計8t/計画20t）
```

