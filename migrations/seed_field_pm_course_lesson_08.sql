## 📋 このレッスンで学ぶこと

現場の成否は「コミュニケーション」で決まります。本レッスンでは、報連相（報告・連絡・相談）の仕組み化、朝礼・定例会議の運営、情報共有ツールの活用など、実践的なコミュニケーション手法を学びます。

